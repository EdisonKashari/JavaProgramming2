package Donjeta_Tasks;

public class Even_Or_Odd_Number {
    public static void main(String[] args) {
        int number = 50;

        for (int i = 0; i <= number; i++) {
            if (i%2==0){
                System.out.print(i+" are even number");

            }else {
                System.out.print(i);
            }



        }
    }
}
