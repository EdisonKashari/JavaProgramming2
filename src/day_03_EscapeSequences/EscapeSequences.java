package day_03_EscapeSequences;

/*
escape sequences:Must be given with double quote
\n: starts a new line
\t: tab bottom
\\: backslash
\": double code
 */
public class EscapeSequences {
    public static void main(String[] args){
        System.out.println("Java \nPython\nC#");

        System.out.println("-------------------");

        System.out.println("Hello Cydeo \nHow are you today?\nIt's nice to see you all!\nWhat class do we have next week");

        System.out.println("-------------------");

        System.out.println("\t\tJava is cool Programming Language");

        System.out.println("-------------------");

        System.out.println("// \\\\"); // in order to print one back slash we need to give two backward slash

        System.out.println("-------------------");

        System.out.println("My favorite show is \"Game of Throne\""); //










    }

}
